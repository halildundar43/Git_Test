package day01_variables;

public class Variable02 {

	public static void main(String[] args) {
		
		char harf1 = 'a'; // küçük a deðeri 97
		char harf2 = 'b'; // küçük b deðeri 98
		System.out.println(harf1+harf2);
		
		System.out.print(harf1);
		System.out.println(harf2);
		
		int aCharDeger='a';
		System.out.println(aCharDeger);
		
		int charDegerü='!';
		System.out.println(charDegerü);
		
		int charDegery='%';
		System.out.println(charDegery);
		
		int charDegeras='*';
		System.out.println(charDegeras);
		
		
	}

}
