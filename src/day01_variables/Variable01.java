package day01_variables;

public class Variable01 {

	public static void main(String[] args) {
		
		int maas = 3000;
		char harf = 'S'; // char'lara deðer atarken tek sembol kullanýlacak ve tek týrnaklara koy
		
		System.out.println(maas); // ekranda 3000 göreceksin
		System.out.println(harf); // ekranda S göreceksin
		
		boolean isOld = true; // boolean'lar için sadece true ve false kullanabiliriz
		System.out.println(isOld);
		
		boolean isNew = false;
		System.out.println(isNew);
		
		byte derinlik = -123;
		System.out.println(derinlik);
		
		short nazilliNüfus = 28000;
		System.out.println(nazilliNüfus);
		
		long hucreSayisi = 9000000000l; //integer sýnýrýný aþan sayýlarda sonuna l veya L koymalýyýz
		System.out.println(hucreSayisi);
		
		double pi = 3.1434656787889;
		System.out.println(pi);
		
		float para=5.25f;
		System.out.println(para);
		
		String ogrenciIsmi="Ali Can";
		System.out.println(ogrenciIsmi);
		
		
		
	}
}
