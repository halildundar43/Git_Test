package day05_operators;

public class Increment04 {

	public static void main(String[] args) {
		// int data type da bir tane veriable olusturun
		// bu veriable her seferinde 1 arttırmak için 3 yöntemide kullanın
		
	int num1 = 12;
	num1 = num1 + 1;
	System.out.println(num1); //num1:13
	
	num1+=1; ////num1:13
	System.out.println(num1); //num1:14
	
	num1++; //num1:14
	System.out.println(num1); //num1:15

	}

}
