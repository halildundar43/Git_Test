package day05_operators;

public class Increment01 {

	public static void main(String[] args) {
		// int data tipinde 2 variable olusturun 
		// 1.yi 3 2.yi 7 arttýrýn
		
	int sayi1 = 12;
	sayi1 = sayi1 + 3; // increment birinci ve uzun yol
	System.out.println(sayi1);
	
	int sayi2 = 20;
	sayi2 = sayi2 + 7; // increment birinci ve uzun yol
	System.out.println(sayi2);
	
	}

}
