package day22_constructor;

public class Car {

	String marka;
	String model;
	int yıl;
	boolean kazasıVarMı;

	public static void main(String[] args) {

		Car car1 = new Car();
		System.out.println(car1.marka + " " + car1.model + " " + car1.yıl + " " + car1.kazasıVarMı); // default değerler yazar

		// Bir object olusturuldugunda esitligin saginda mutlaka Constructor kullanilir
		// Constructor ismi clas ismi ile ayni olmalidir
		// Constructor'da mutlaka () olmalidir
		// Constructor'da parametre olmasi istege baglidir

		car1.marka = "Toyota";
		car1.model = "Corolla";
		car1.yıl = 2010;
		car1.kazasıVarMı = false;
		System.out.println(car1.marka + " " + car1.model + " " + car1.yıl + " " + car1.kazasıVarMı);

		Car car2 = new Car();
		car2.marka = "Opel";
		car2.model = "Astra";
		car2.yıl = 2015;
		car2.kazasıVarMı = true;
		System.out.println(car2.marka + " " + car2.model + " " + car2.yıl + " " + car2.kazasıVarMı);

		car1.kazasıVarMı = true; // car1 e ait herhangi bir özelliği değiştirebiliriz
		System.out.println(car1.marka + " " + car1.model + " " + car1.yıl + " " + car1.kazasıVarMı);

	}

}
